const { Schema,model } = require('mongoose')

const schema = new Schema({
    totalStudents:{
        type:String,
        trim:true
    },
    workingDays:{
        type:String,
        trim:true
    },
    presentDays:{
        type:String,
        trim:true
    },
    session:{
        type:String,
        trim:true
    },
    types:{
        type:String,
        trim:true,
        required:true
    },
    result:{
        type:String,
        trim:true,
        required:true
    },
    gpa:{
        type:String,
        trim:true,
        required:true
    },
    rank:{
        type:String,
        trim:true 
    },
    comments:{
        type:String,
        trim:true
    },
    signatureOfPrinciple:String,
    signatureOfTeacher:String,
    approved:{
        type:Boolean
    },
    submited:{
        type:Boolean
    },
    rejected:{
        Boolean
    },
    passedOrFailed:{
        type:Boolean
    },
    dateOfResultPublication:{
        type:String,
        trim:true
    },
    subjects:[
        {
            name:{
                type:String,
                trim:String,
                required:true 
            },
            obtainedMarks:{
                type:String,
                trim:String,
                required:true   
            },
            grade:{
                type:String,
                trim:String,
                required:true 
            },
            passedMarks:String,
            fullMarks:String,
        }
    ],
    Student:{
        type:Schema.Types.ObjectId,
        ref:'Student'
    }

})

const Result = new model('Result',schema)
module.exports = Result